#!/bin/bash
set -e
set -o pipefail

OCP_CLUSTER_NAME=@@{ocp_cluster_name}@@

export KUBECONFIG=$HOME/.kube/$OCP_CLUSTER_NAME.cfg
OCP_BUILD_CACHE_BASE=.local/$OCP_CLUSTER_NAME/.build-cache

mkdir -p $OCP_BUILD_CACHE_BASE

## If you add user-monitoring you can also add a Grafana Dashboard like this:
cat <<EOF | kubectl apply -f -
kind: ConfigMap
apiVersion: v1
metadata:
  name: observability-metrics-custom-allowlist
  namespace: open-cluster-management-observability
data:
  metrics_list.yaml: |
    names:
      - nutanix_objectstore_total_requests_sec
EOF

